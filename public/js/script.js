// Fungsi untuk menangani peristiwa klik
function toggleList() {
    // Dapatkan elemen list
    var listKegiatan = document.getElementById('list-kegiatan');

    // Periksa apakah list sedang ditampilkan atau sembunyi
    if (listKegiatan.style.display === 'none') {
        // Jika sedang sembunyi, tampilkan
        listKegiatan.style.display = 'block';
    } else {
        // Jika sedang ditampilkan, sembunyikan
        listKegiatan.style.display = 'none';
    }
}
